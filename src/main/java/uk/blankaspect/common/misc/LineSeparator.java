package uk.blankaspect.common.misc;

//----------------------------------------------------------------------


// LINE SEPARATOR ENUMERATION


public enum LineSeparator
	implements IStringKeyed
{

////////////////////////////////////////////////////////////////////////
//  Constants
////////////////////////////////////////////////////////////////////////

	CR
	(
		"cr",
		"CR",
		"\r"
	),

	LF
	(
		"lf",
		"LF",
		"\n"
	),

	CR_LF
	(
		"crlf",
		"CR+LF",
		"\r\n"
	);

////////////////////////////////////////////////////////////////////////
//  Constructors
////////////////////////////////////////////////////////////////////////

	private LineSeparator(String key,
						  String text,
						  String separator)
	{
		this.key = key;
		this.text = text;
		this.separator = separator;
	}

	//------------------------------------------------------------------

////////////////////////////////////////////////////////////////////////
//  Class methods
////////////////////////////////////////////////////////////////////////

	public static LineSeparator forKey(String key)
	{
		for (LineSeparator value : values())
		{
			if (value.key.equals(key))
				return value;
		}
		return null;
	}

	//------------------------------------------------------------------

////////////////////////////////////////////////////////////////////////
//  Instance methods : IStringKeyed interface
////////////////////////////////////////////////////////////////////////

	public String getKey()
	{
		return key;
	}

	//------------------------------------------------------------------

////////////////////////////////////////////////////////////////////////
//  Instance methods : overriding methods
////////////////////////////////////////////////////////////////////////

	@Override
	public String toString()
	{
		return text;
	}

	//------------------------------------------------------------------

////////////////////////////////////////////////////////////////////////
//  Instance methods
////////////////////////////////////////////////////////////////////////

	public String getSeparator()
	{
		return separator;
	}

	//------------------------------------------------------------------

////////////////////////////////////////////////////////////////////////
//  Instance variables
////////////////////////////////////////////////////////////////////////

	private	String	key;
	private	String	text;
	private	String	separator;

}

